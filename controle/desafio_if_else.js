const a = 10
const b = 5
let operacao = '%'

if (operacao === '+') {
    console.log(a + b)
} else if (operacao === '-') {
    console.log(a - b)
} else if (operacao === '*') {
    console.log(a * b)
} else if (operacao === '/') {
    console.log(a / b)
} else {
    console.log(a % b)
}
console.log('fim')